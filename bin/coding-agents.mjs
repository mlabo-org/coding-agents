#!/usr/bin/env node

import { execFileSync } from "node:child_process";
import { existsSync, mkdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import path from "node:path";

const TOOL = "coding-agents";
const GENERATED_HEADING = "# Generated by coding-agents MVP";
const START = "<!-- coding-agents-mvp:start -->";
const END = "<!-- coding-agents-mvp:end -->";

const ROLES = [
  "Intake",
  "Repo Mapper",
  "Requirements",
  "Planner",
  "Architect",
  "Implementer",
  "Test Runner",
  "Reviewer",
  "Risk Guard",
  "Docs Keeper",
  "UX",
  "Dependency",
  "DevOps",
  "Auditor",
];

const REQUIRED_FILES = [
  "README.md",
  "project.md",
  "task.md",
  "todo.md",
  "decisions.md",
  "audit.md",
  "assignments.md",
  "handoff.md",
];

function main() {
  const args = parseArgs(process.argv.slice(2));
  if (args.help || !args.command) {
    printHelp();
    process.exit(0);
  }

  try {
    if (args.command === "intake") intake(args);
    else if (args.command === "handoff") handoff(args);
    else if (args.command === "doctor") doctor(args);
    else fail(`unknown command: ${args.command}`, 1);
  } catch (error) {
    fail(error.message, error.exitCode ?? 1);
  }
}

function parseArgs(argv) {
  const parsed = { command: null, help: false };
  for (let i = 0; i < argv.length; i += 1) {
    const token = argv[i];
    if (token === "--help" || token === "-h") {
      parsed.help = true;
      continue;
    }
    if (!parsed.command && !token.startsWith("--")) {
      parsed.command = token;
      continue;
    }
    if (token.startsWith("--")) {
      const key = token.slice(2);
      const value = argv[i + 1];
      if (!value || value.startsWith("--")) {
        throw new CliError(`missing value for --${key}`);
      }
      parsed[toCamel(key)] = value;
      i += 1;
      continue;
    }
    throw new CliError(`unexpected argument: ${token}`);
  }
  return parsed;
}

function intake(args) {
  const cwd = requireCwd(args.cwd);
  const task = requireArg(args.task, "--task");
  const taskId = requireArg(args.taskId, "--task-id");
  const epoch = requireArg(args.epoch, "--epoch");
  const scope = requireArg(args.scope, "--scope");
  const docsDir = path.join(cwd, "docs", "codex");
  mkdirSync(docsDir, { recursive: true });

  const context = {
    cwd,
    task,
    taskId,
    epoch,
    scope,
    docsDir,
    timestamp: new Date().toISOString(),
    gitStatus: readGitStatus(cwd),
  };

  for (const [file, body] of Object.entries(renderDocs(context))) {
    upsertGenerated(path.join(docsDir, file), body);
  }

  console.log(`ok intake: updated ${docsDir}`);
  console.log(`ok task_id: ${taskId}`);
  console.log(`ok epoch: ${epoch}`);
  console.log(`ok scope: ${scope}`);
}

function handoff(args) {
  const cwd = requireCwd(args.cwd);
  requireArg(args.taskId, "--task-id");
  const handoffPath = path.join(cwd, "docs", "codex", "handoff.md");
  if (!existsSync(handoffPath)) {
    throw new CliError(`missing handoff file: ${handoffPath}`, 1);
  }
  process.stdout.write(readFileSync(handoffPath, "utf8"));
}

function doctor(args) {
  const cwd = requireCwd(args.cwd);
  const docsDir = path.join(cwd, "docs", "codex");
  const results = [];
  let fatal = false;

  if (!existsSync(docsDir)) {
    results.push(["warn", `missing docs directory: ${docsDir}`]);
    fatal = true;
  } else {
    results.push(["ok", `docs directory exists: ${docsDir}`]);
  }

  for (const file of REQUIRED_FILES) {
    const filePath = path.join(docsDir, file);
    if (existsSync(filePath)) results.push(["ok", `required file exists: ${file}`]);
    else {
      results.push(["warn", `missing required file: ${file}`]);
      fatal = true;
    }
  }

  const assignmentPath = path.join(docsDir, "assignments.md");
  if (existsSync(assignmentPath)) {
    const text = readFileSync(assignmentPath, "utf8");
    const missingRoles = ROLES.filter((role) => !new RegExp(`^## ${escapeRegExp(role)}$`, "m").test(text));
    const missingFields = [];
    for (const role of ROLES) {
      const section = getRoleSection(text, role);
      for (const field of ["role", "status", "task_id", "epoch", "scope", "assignment", "expected_output"]) {
        if (!new RegExp(`^- ${field}:`, "m").test(section)) {
          missingFields.push(`${role}.${field}`);
        }
      }
    }
    if (missingRoles.length === 0) results.push(["ok", "14 role assignments present"]);
    else {
      results.push(["warn", `missing role assignments: ${missingRoles.join(", ")}`]);
      fatal = true;
    }
    if (missingFields.length === 0) results.push(["ok", "assignment fields include role/status/task_id/epoch/scope/assignment/expected_output"]);
    else {
      results.push(["warn", `missing assignment fields: ${missingFields.join(", ")}`]);
      fatal = true;
    }
  }

  const taskPath = path.join(docsDir, "task.md");
  if (existsSync(taskPath)) {
    const text = readFileSync(taskPath, "utf8");
    for (const key of ["task_id", "epoch", "scope"]) {
      if (new RegExp(`${key}:`).test(text)) results.push(["ok", `${key} present in task.md`]);
      else {
        results.push(["warn", `${key} missing from task.md`]);
        fatal = true;
      }
    }
  }

  const gitStatus = readGitStatus(cwd);
  if (gitStatus.ok) {
    const status = gitStatus.output.trim();
    if (status) results.push(["warn", `git has uncommitted changes (${status.split("\n").length} line(s))`]);
    else results.push(["ok", "git working tree clean"]);
  } else {
    results.push(["warn", `git status unavailable: ${gitStatus.error}`]);
  }

  for (const [level, message] of results) {
    console.log(`${level} ${message}`);
  }
  process.exit(fatal ? 1 : 0);
}

function renderDocs(context) {
  return {
    "README.md": renderReadme(context),
    "project.md": renderProject(context),
    "task.md": renderTask(context),
    "todo.md": renderTodo(context),
    "decisions.md": renderDecisions(context),
    "audit.md": renderAudit(context),
    "assignments.md": renderAssignments(context),
    "handoff.md": renderHandoff(context),
  };
}

