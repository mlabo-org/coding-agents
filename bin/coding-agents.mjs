function getAnyFieldValues(section, field) {
  const values = [];
  for (const block of getStructuralFieldBlocks(section)) {
    for (const entry of block) {
      if (entry.field !== field) continue;
      const value = entry.value.trim();
      if (value) values.push(value);
    }
  }
  return values;
}

function getStructuralFieldBlocks(section) {
  const blocks = [];
  let current = [];
  let inFence = false;
  for (const line of String(section || "").split(/\r?\n/)) {
    if (/^\s*(?:```|~~~)/.test(line)) {
      if (current.length) {
        blocks.push(current);
        current = [];
      }
      inFence = !inFence;
      continue;
    }
    if (inFence) continue;
    if (line.trim() === "") continue;
    const match = /^- ([A-Za-z0-9_][A-Za-z0-9_.-]*):\s*(.*)$/.exec(line);
    if (match) {
      current.push({ field: match[1], value: match[2] });
      continue;
    }
    if (current.length) {
      blocks.push(current);
      current = [];
    }
  }
  if (current.length) blocks.push(current);
  return blocks;
}

function getMetacognitiveGateFieldSection(text) {
  const content = String(text || "");
  const headingPattern = new RegExp(`^(?:#{1,6}\\s+)?${escapeRegExp(METACOGNITIVE_GATE_NAME)}:?\\s*$`);
  const heading = findMarkdownLineOutsideFences(content, (line) => headingPattern.test(line));
  if (!heading) return content;
  const nextHeading = findMarkdownLineOutsideFences(
    content,
    (line) => /^#{1,6}\s+/.test(line),
    heading.end,
  );
  return content.slice(heading.start, nextHeading ? nextHeading.start : content.length);
}

function getCodingConductFieldSection(text) {
  const content = String(text || "");
  const headingPattern = new RegExp(`^(?:#{1,6}\\s+)?${escapeRegExp(CODING_CONDUCT_GATE_NAME)}:?\\s*$`);
  const heading = findMarkdownLineOutsideFences(content, (line) => headingPattern.test(line));
  if (!heading) return content;
  const nextHeading = findMarkdownLineOutsideFences(
    content,
    (line) => /^#{1,6}\s+/.test(line) || /^(?:Debugging integrity|Subagent lifecycle|Meta-Cognitive Debug\/Repair Gate):$/.test(line),
    heading.end,
  );
  return content.slice(heading.start, nextHeading ? nextHeading.start : content.length);
}

function findMarkdownLineOutsideFences(content, predicate, from = 0) {
  let inFence = false;
  const linePattern = /^.*(?:\r?\n|$)/gm;
  for (const match of content.matchAll(linePattern)) {
    if (match[0] === "") continue;
    const start = match.index;
    const rawLine = match[0];
    const line = rawLine.replace(/\r?\n$/, "");
    const end = start + line.length;
    if (/^\s*(?:```|~~~)/.test(line)) {
      inFence = !inFence;
      continue;
    }
    if (end < from) continue;
    if (!inFence && start >= from && predicate(line)) return { start, end };
  }
  return null;
}

function readIdentityField(section, field, label) {
  const values = getFieldValues(section, field);
  return {
    value: values[0] || null,
    errors: identityFieldErrors(values, field, label, { missing: false }),
  };
}

function validateIdentityField(section, field, label) {
  return identityFieldErrors(getFieldValues(section, field), field, label, { missing: true });
}

function identityFieldErrors(values, field, label, options = {}) {
  const errors = [];
  if (options.missing && values.length === 0) errors.push(`${label}.${field}`);
  if (values.length > 1) errors.push(`${label}.${field} duplicated`);
  for (const value of values) {
    if (/[\r\n]/.test(value)) errors.push(`${label}.${field} contains CR/LF`);
  }
  return errors;
}

