function fail(message, code) {
  console.error(`${TOOL}: ${message}`);
  process.exit(code);
}

function toCamel(value) {
  return value.replace(/-([a-z])/g, (_, char) => char.toUpperCase());
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function getRoleSection(text, role) {
  const startMatch = new RegExp(`^## ${escapeRegExp(role)}$`, "m").exec(text);
  if (!startMatch) return "";
  const start = startMatch.index;
  const next = text.slice(start + startMatch[0].length).search(/^## /m);
  if (next === -1) return text.slice(start);
  return text.slice(start, start + startMatch[0].length + next);
}

function getFieldValue(section, field) {
  const match = new RegExp(`^- ${escapeRegExp(field)}:\\s*(.*)$`, "m").exec(section);
  if (!match) return null;
  const value = match[1].trim();
  return value ? value : null;
}

function singleLine(value) {
  return String(value).replace(/\s+/g, " ").trim();
}

function summarizeRunnerOutput(value) {
  const text = singleLine(value || "none");
  if (text.length <= 600) return text;
  return `${text.slice(0, 597)}...`;
}

function formatExitCode(value) {
  return typeof value === "number" ? String(value) : "none";
}

function ensureTrailingNewline(value) {
  return value.endsWith("\n") ? value : `${value}\n`;
}

function parsePositiveInt(value, flag) {
  const parsed = Number.parseInt(String(value), 10);
  if (!Number.isSafeInteger(parsed) || parsed <= 0) {
    throw new CliError(`invalid ${flag}: expected positive integer`, 1);
  }
  return parsed;
}

class CliError extends Error {
  constructor(message, exitCode = 1) {
    super(message);
    this.exitCode = exitCode;
  }
}

