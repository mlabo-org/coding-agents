function normalizeDebuggingIntegrity(args) {
  const commandContext = resolveCommandContext(args);
  const state = resolveWorkflowState(commandContext.targetCwd);
  const stateDir = state.stateDir;
  if (!existsSync(stateDir)) {
    throw new CliError(`missing workflow state directory: ${stateDir}`, 1);
  }

  const normalizers = {
    "README.md": normalizeReadmeDebugIntegrity,
    "task.md": normalizeTaskDebugIntegrity,
    "audit.md": normalizeAuditDebugIntegrity,
    "assignments.md": normalizeAssignmentsDebugIntegrity,
    "handoff.md": normalizeHandoffDebugIntegrity,
    [RUNNER_FILE]: normalizeRunnerDebugIntegrity,
  };
  const updates = [];

  for (const [file, normalize] of Object.entries(normalizers)) {
    const filePath = path.join(stateDir, file);
    if (!existsSync(filePath)) continue;
    const current = readFileSync(filePath, "utf8");
    const next = normalize(current);
    if (next !== current) updates.push({ file, filePath, next });
  }

  const mode = args.execute ? "EXECUTE" : "DRY-RUN";
  console.log(`Mode: ${mode}`);
  console.log(`State dir: ${stateDir}`);
  if (updates.length === 0) {
    console.log("No debugging integrity normalization needed.");
    printLegacyHints(state);
    return;
  }

  for (const update of updates) {
    console.log(`${args.execute ? "Updated" : "Would update"}: ${update.file}`);
  }
  if (!args.execute) {
    console.log("No changes made. Re-run with --execute to normalize existing workflow state.");
    printLegacyHints(state);
    return;
  }

  prepareStateWrite(state);
  for (const update of updates) {
    writeFileSync(update.filePath, ensureTrailingNewline(update.next), "utf8");
  }
  finalizeStateWrite(state);
  printLegacyHints(state);
}

function doctor(args) {
  const commandContext = resolveCommandContext(args);
  const state = resolveWorkflowState(commandContext.targetCwd);
  const stateDir = state.stateDir;
  const results = [];
  let fatal = false;

  results.push(["ok", `invocation cwd: ${commandContext.invocationCwd}`]);
  results.push(["ok", `target cwd: ${commandContext.targetCwd}`]);

  if (!existsSync(stateDir)) {
    results.push(["warn", `missing workflow state directory: ${stateDir}`]);
    fatal = true;
  } else {
    results.push(["ok", `workflow state directory exists: ${stateDir}`]);
  }

  for (const file of REQUIRED_FILES) {
    const filePath = path.join(stateDir, file);
    if (existsSync(filePath)) results.push(["ok", `required file exists: ${file}`]);
    else {
      results.push(["warn", `missing required file: ${file}`]);
      fatal = true;
    }
  }

  const assignmentValidation = validateAssignmentFiles(stateDir);
  results.push(...assignmentValidation.results);
  fatal = fatal || assignmentValidation.fatal;

  const taskPath = path.join(stateDir, "task.md");
  if (existsSync(taskPath)) {
    const text = readFileSync(taskPath, "utf8");
    for (const key of ["task_id", "epoch", "scope"]) {
      if (new RegExp(`${key}:`).test(text)) results.push(["ok", `${key} present in task.md`]);
      else {
        results.push(["warn", `${key} missing from task.md`]);
        fatal = true;
      }
    }
  }

  const gitStatus = readGitStatus(commandContext.targetCwd);
  if (gitStatus.ok) {
    const status = gitStatus.output.trim();
    if (status) results.push(["warn", `git has uncommitted changes (${status.split("\n").length} line(s))`]);
    else results.push(["ok", "git working tree clean"]);
  } else {
    results.push(["warn", `git status unavailable: ${gitStatus.error}`]);
  }

  for (const [level, message] of results) {
    console.log(`${level} ${message}`);
  }
  printLegacyHints(state);
  process.exit(fatal ? 1 : 0);
}

