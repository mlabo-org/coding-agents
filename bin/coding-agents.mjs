function upsertGenerated(filePath, body) {
  const block = `${START}\n${GENERATED_HEADING}\n\n${body.trim()}\n${END}\n`;
  if (!existsSync(filePath)) {
    writeFileSync(filePath, block, "utf8");
    return;
  }

  const current = readFileSync(filePath, "utf8");
  if (current.startsWith(GENERATED_HEADING)) {
    writeFileSync(filePath, block, "utf8");
    return;
  }

  const startIndex = current.indexOf(START);
  const endIndex = current.indexOf(END);
  if (startIndex !== -1 && endIndex !== -1 && endIndex > startIndex) {
    const before = current.slice(0, startIndex).trimEnd();
    const after = current.slice(endIndex + END.length).trimStart();
    const next = [before, block.trimEnd(), after].filter(Boolean).join("\n\n");
    writeFileSync(filePath, `${next}\n`, "utf8");
    return;
  }

  writeFileSync(filePath, `${current.trimEnd()}\n\n${block}`, "utf8");
}

function requireCwd(value) {
  const cwd = path.resolve(requireArg(value, "--cwd"));
  if (!existsSync(cwd)) throw new CliError(`cwd does not exist: ${cwd}`, 1);
  if (!statSync(cwd).isDirectory()) throw new CliError(`cwd is not a directory: ${cwd}`, 1);
  return cwd;
}

function requireArg(value, flag) {
  if (!value || !String(value).trim()) throw new CliError(`missing required argument: ${flag}`, 1);
  return String(value);
}

function readGitStatus(cwd) {
  try {
    const output = execFileSync("git", ["-C", cwd, "status", "--short"], {
      encoding: "utf8",
      stdio: ["ignore", "pipe", "pipe"],
    });
    return { ok: true, output };
  } catch (error) {
    return { ok: false, error: String(error.stderr || error.message).trim() };
  }
}

function summarizeGit(output) {
  const trimmed = output.trim();
  if (!trimmed) return "clean";
  return `${trimmed.split("\n").length} changed line(s)`;
}

function printHelp() {
  console.log(`coding-agents MVP CLI

Usage:
  node bin/coding-agents.mjs intake --cwd <path> --task <text> --task-id <id> --epoch <epoch> --scope <scope>
  node bin/coding-agents.mjs handoff --cwd <path> --task-id <id>
  node bin/coding-agents.mjs doctor --cwd <path>
  node bin/coding-agents.mjs --help

Commands:
  intake   Create or update target docs/codex workflow files.
  handoff  Print target docs/codex/handoff.md.
  doctor   Check required docs, role assignments, isolation keys, and Git state.
`);
}

function fail(message, code) {
  console.error(`${TOOL}: ${message}`);
  process.exit(code);
}

function toCamel(value) {
  return value.replace(/-([a-z])/g, (_, char) => char.toUpperCase());
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function getRoleSection(text, role) {
  const startMatch = new RegExp(`^## ${escapeRegExp(role)}$`, "m").exec(text);
  if (!startMatch) return "";
  const start = startMatch.index;
  const next = text.slice(start + startMatch[0].length).search(/^## /m);
  if (next === -1) return text.slice(start);
  return text.slice(start, start + startMatch[0].length + next);
}

class CliError extends Error {
  constructor(message, exitCode = 1) {
    super(message);
    this.exitCode = exitCode;
  }
}

main();
