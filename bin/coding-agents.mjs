function insertFieldBeforeLifecycle(section, field, value) {
  return upsertFieldBeforeLifecycle(section, field, value);
}

function upsertFieldBeforeLifecycle(section, field, value, options = {}) {
  const line = `- ${field}: ${value}`;
  const pattern = new RegExp(`^- ${escapeRegExp(field)}:\\s*(.*)$`, "m");
  const match = pattern.exec(section);
  if (match) {
    const current = match[1].trim();
    if (options.replace || (options.replaceIfEmptyOrNone && (!current || current === "none"))) {
      return section.replace(pattern, line);
    }
    return section;
  }
  if (/^- lifecycle:/m.test(section)) return section.replace(/^- lifecycle:/m, `${line}\n- lifecycle:`);
  return `${section.trimEnd()}\n${line}\n`;
}

function insertAfterHeading(text, heading, block) {
  const pattern = new RegExp(`^${escapeRegExp(heading)}$`, "m");
  if (!pattern.test(text)) return `${text.trimEnd()}\n\n${block}\n`;
  return text.replace(pattern, `${heading}\n\n${block}`);
}

function insertAfterHeadingSection(text, heading, block) {
  const pattern = new RegExp(`^${escapeRegExp(heading)}$`, "m");
  const match = pattern.exec(text);
  if (!match) return `${text.trimEnd()}\n\n${block}\n`;
  const afterHeadingStart = match.index + match[0].length;
  const nextHeadingOffset = text.slice(afterHeadingStart).search(/^## /m);
  if (nextHeadingOffset === -1) return `${text.trimEnd()}\n\n${block}\n`;
  const insertAt = afterHeadingStart + nextHeadingOffset;
  return `${text.slice(0, insertAt).trimEnd()}\n\n${block}\n\n${text.slice(insertAt).trimStart()}`;
}

function insertAfterRoleScaffoldHeading(text, block) {
  for (const heading of ["# Role Assignment Scaffold", "# Role Assignments"]) {
    if (new RegExp(`^${escapeRegExp(heading)}$`, "m").test(text)) {
      return insertAfterHeading(text, heading, block);
    }
  }
  return `${text.trimEnd()}\n\n${block}\n`;
}

function insertAfterLineMatching(text, pattern, block, fallback) {
  if (pattern.test(text)) return text.replace(pattern, (line) => `${line}\n${block}`);
  return `${text.trimEnd()}\n\n${fallback}\n`;
}

