function appendUnderHeading(text, heading, entry) {
  const headingLine = `## ${heading}`;
  const trimmedEntry = entry.trim();
  if (!new RegExp(`^${escapeRegExp(headingLine)}$`, "m").test(text)) {
    return `${text.trimEnd()}\n\n${headingLine}\n\n${trimmedEntry}\n`;
  }

  const headingMatch = new RegExp(`^${escapeRegExp(headingLine)}$`, "m").exec(text);
  const afterHeadingStart = headingMatch.index + headingMatch[0].length;
  const nextHeadingOffset = text.slice(afterHeadingStart).search(/^## /m);
  if (nextHeadingOffset === -1) {
    return `${text.trimEnd()}\n\n${trimmedEntry}\n`;
  }
  const insertAt = afterHeadingStart + nextHeadingOffset;
  const before = text.slice(0, insertAt).trimEnd();
  const after = text.slice(insertAt).trimStart();
  return `${before}\n\n${trimmedEntry}\n\n${after}`;
}

function validateAssignmentFiles(stateDir) {
  const results = [];
  let fatal = false;
  let checkedFiles = 0;
  const assignmentPath = path.join(stateDir, "assignments.md");

  if (!existsSync(stateDir)) {
    return { results: [["warn", `missing workflow state directory: ${stateDir}`]], fatal: true };
  }

  if (existsSync(assignmentPath)) {
    checkedFiles += 1;
    const text = readFileSync(assignmentPath, "utf8");
    const roleValidation = validateRoleAssignments(text);
    results.push(...roleValidation.results);
    fatal = fatal || roleValidation.fatal;
  }

  const runnerPath = path.join(stateDir, RUNNER_FILE);
  if (existsSync(runnerPath)) {
    checkedFiles += 1;
    const runnerValidation = validateRunnerPackets(readFileSync(runnerPath, "utf8"));
    results.push(...runnerValidation.results);
    fatal = fatal || runnerValidation.fatal;
  }

  if (checkedFiles === 0) {
    results.push(["warn", `no assignment files found in ${stateDir}`]);
    fatal = true;
  }

  return { results, fatal };
}

function validateRoleAssignments(text) {
  const results = [];
  let fatal = false;
  const missingRoles = ROLES.filter((role) => !new RegExp(`^## ${escapeRegExp(role)}$`, "m").test(text));
  const invalidFields = [];

  for (const role of ROLES) {
    const section = getRoleSection(text, role);
    for (const field of ["role", "status", "task_id", "epoch", "scope", "assignment", "expected_output", "lifecycle"]) {
      if (!getFieldValue(section, field)) {
        invalidFields.push(`${role}.${field}`);
      }
    }
  }

  if (missingRoles.length === 0) results.push(["ok", "14 role assignments present"]);
  else {
    results.push(["warn", `missing role assignments: ${missingRoles.join(", ")}`]);
    fatal = true;
  }

  if (invalidFields.length === 0) results.push(["ok", "assignment fields are present and non-empty"]);
  else {
    results.push(["warn", `missing or empty assignment fields: ${invalidFields.join(", ")}`]);
    fatal = true;
  }

  if (text.includes(DEBUG_INTEGRITY)) results.push(["ok", "debugging integrity gate present"]);
  else {
    results.push(["warn", "debugging integrity gate missing from assignments"]);
    fatal = true;
  }

  return { results, fatal };
}

function validateRunnerPackets(text) {
  const sections = text.split(/^### /m).slice(1).map((section) => `### ${section}`);
  const invalidPackets = [];
  let checked = 0;

  for (const section of sections) {
    const type = getFieldValue(section, "type");
    if (!["assignment", "parent-integration", "process-orchestration-skeleton", "process-runner-result"].includes(type)) continue;
    checked += 1;
    for (const field of ["role", "task_id", "epoch", "scope"]) {
      if (!getFieldValue(section, field)) {
        invalidPackets.push(`${section.split("\n")[0].replace(/^### /, "")}.${field}`);
      }
    }
    if (type === "assignment" || type === "process-orchestration-skeleton") {
      for (const field of ["assignment", "expected_output", "debugging_integrity", "lifecycle"]) {
        if (!getFieldValue(section, field)) {
          invalidPackets.push(`${section.split("\n")[0].replace(/^### /, "")}.${field}`);
        }
      }
    }
    if (type === "parent-integration") {
      for (const field of ["status", "debugging_integrity", "lifecycle"]) {
        if (!getFieldValue(section, field)) {
          invalidPackets.push(`${section.split("\n")[0].replace(/^### /, "")}.${field}`);
        }
      }
    }
    if (type === "process-runner-result") {
      for (const field of ["status", "runner", "spawned", "exit_code", "summary", "failure", "debugging_integrity", "lifecycle"]) {
        if (!getFieldValue(section, field)) {
          invalidPackets.push(`${section.split("\n")[0].replace(/^### /, "")}.${field}`);
        }
      }
    }
  }

  if (invalidPackets.length === 0) {
    return { results: [["ok", `runner packets valid (${checked} checked)`]], fatal: false };
  }
  return { results: [["warn", `missing or empty runner packet fields: ${invalidPackets.join(", ")}`]], fatal: true };
}

function renderRunnerLifecycle(result) {
  if (result.status === "completed") {
    return "Parent integrates the completed result, then closes or retires the subagent unless an explicitly scoped continuation is required.";
  }
  return "Parent records the failure, timeout, or blocker, retires this subagent context, and issues a fresh scoped assignment only if more work is still required.";
}

function upsertGenerated(filePath, body) {
  const block = `${START}\n${GENERATED_HEADING}\n\n${body.trim()}\n${END}\n`;
  if (!existsSync(filePath)) {
    writeFileSync(filePath, block, "utf8");
    return;
  }

  const current = readFileSync(filePath, "utf8");
  if (current.startsWith(GENERATED_HEADING)) {
    writeFileSync(filePath, block, "utf8");
    return;
  }

  const startIndex = current.indexOf(START);
  const endIndex = current.indexOf(END);
  if (startIndex !== -1 && endIndex !== -1 && endIndex > startIndex) {
    const before = current.slice(0, startIndex).trimEnd();
    const after = current.slice(endIndex + END.length).trimStart();
    const next = [before, block.trimEnd(), after].filter(Boolean).join("\n\n");
    writeFileSync(filePath, `${next}\n`, "utf8");
    return;
  }

  writeFileSync(filePath, `${current.trimEnd()}\n\n${block}`, "utf8");
}

function resolveWorkflowState(cwd) {
  const gitRoot = resolveGitRoot(cwd);
  if (!gitRoot) {
    throw new CliError(
      `workflow state requires a Git worktree: ${cwd} is not inside a Git worktree/root; run with --cwd inside a Git repository or set --target-cwd to the target repository`,
      1,
    );
  }
  const stateRoot = gitRoot;
  const defaultStateDir = path.join(stateRoot, STATE_DIR_NAME);
  return {
    cwd,
    gitRoot,
    stateRoot,
    defaultStateDir,
    stateDir: defaultStateDir,
    legacyDirs: findLegacyStateDirs(cwd, gitRoot),
  };
}

function resolveGitRoot(cwd) {
  try {
    const output = execFileSync("git", ["-C", cwd, "rev-parse", "--show-toplevel"], {
      encoding: "utf8",
      stdio: ["ignore", "pipe", "pipe"],
    }).trim();
    return output ? path.resolve(output) : null;
  } catch {
    return null;
  }
}

function findLegacyStateDirs(cwd, gitRoot) {
  const candidates = [path.join(cwd, ...LEGACY_DOCS_SEGMENTS)];
  if (gitRoot) candidates.push(path.join(gitRoot, ...LEGACY_DOCS_SEGMENTS));
  const found = [];
  const seen = new Set();
  for (const candidate of candidates) {
    const resolved = path.resolve(candidate);
    if (!existsSync(resolved)) continue;
    const real = realpathSync(resolved);
    if (seen.has(real)) continue;
    seen.add(real);
    found.push(real);
  }
  return found;
}

function prepareStateWrite(state) {
  if (!state.gitRoot) return;
  ensureStateExcluded(state);
  assertStateDirGitSafe(state);
}

function finalizeStateWrite(state) {
  if (!state.gitRoot) return;
  assertStateDirGitSafe(state);
}

function ensureStateExcluded(state) {
  const excludePath = resolveGitExcludePath(state.gitRoot);
  const pattern = `${STATE_DIR_NAME}/`;
  const current = existsSync(excludePath) ? readFileSync(excludePath, "utf8") : "";
  const hasPattern = current.split(/\r?\n/).some((line) => line.trim() === pattern);
  if (!hasPattern) {
    mkdirSync(path.dirname(excludePath), { recursive: true });
    const separator = current && !current.endsWith("\n") ? "\n" : "";
    writeFileSync(excludePath, `${current}${separator}${pattern}\n`, "utf8");
  }

  const probe = `${STATE_DIR_NAME}/.ignore-probe`;
  const check = spawnSync("git", ["-C", state.gitRoot, "check-ignore", "-q", probe], {
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"],
  });
  if (check.status !== 0) {
    const detail = singleLine(check.stderr || check.error?.message || "pattern did not match");
    throw new CliError(`failed to verify ${pattern} is ignored by ${excludePath}; refusing to write state (${detail})`, 1);
  }
}

function resolveGitExcludePath(gitRoot) {
  const output = readGitOutput(gitRoot, ["rev-parse", "--git-path", "info/exclude"], "resolve git exclude path").trim();
  if (!output) return path.join(gitRoot, ".git", "info", "exclude");
  return path.isAbsolute(output) ? output : path.resolve(gitRoot, output);
}

function assertStateDirGitSafe(state) {
  const tracked = readGitOutput(state.gitRoot, ["ls-files", "--", STATE_DIR_NAME], "check tracked workflow state").trim();
  if (tracked) {
    throw new CliError(`refusing to write state: ${STATE_DIR_NAME} is tracked by Git:\n${tracked}`, 1);
  }

  const status = readGitOutput(state.gitRoot, ["status", "--short", "--", STATE_DIR_NAME], "check workflow state status").trim();
  if (status) {
    throw new CliError(
      `refusing to write state: ${STATE_DIR_NAME} appears in git status after adding ${STATE_DIR_NAME}/ to git info exclude:\n${status}`,
      1,
    );
  }
}

function readGitOutput(gitRoot, args, label) {
  try {
    return execFileSync("git", ["-C", gitRoot, ...args], {
      encoding: "utf8",
      stdio: ["ignore", "pipe", "pipe"],
    });
  } catch (error) {
    const detail = String(error.stderr || error.message).trim();
    throw new CliError(`${label} failed: ${detail}`, 1);
  }
}

