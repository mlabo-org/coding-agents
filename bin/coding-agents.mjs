function main() {
  const args = parseArgs(process.argv.slice(2));
  if (args.help || !args.command) {
    printHelp();
    process.exit(0);
  }

  try {
    if (args.command === "intake") intake(args);
    else if (args.command === "handoff") handoff(args);
    else if (args.command === "doctor") doctor(args);
    else if (args.command === "verify-assignments") verifyAssignments(args);
    else if (args.command === "assign") assign(args);
    else if (args.command === "collect") collect(args);
    else if (args.command === "run" || args.command === "orchestrate") run(args);
    else fail(`unknown command: ${args.command}`, 1);
  } catch (error) {
    fail(error.message, error.exitCode ?? 1);
  }
}

function parseArgs(argv) {
  const parsed = { command: null, help: false };
  for (let i = 0; i < argv.length; i += 1) {
    const token = argv[i];
    if (token === "--help" || token === "-h") {
      parsed.help = true;
      continue;
    }
    if (!parsed.command && !token.startsWith("--")) {
      parsed.command = token;
      continue;
    }
    if (token.startsWith("--")) {
      const key = token.slice(2);
      const value = argv[i + 1];
      if (!value || value.startsWith("--")) {
        throw new CliError(`missing value for --${key}`);
      }
      parsed[toCamel(key)] = value;
      i += 1;
      continue;
    }
    throw new CliError(`unexpected argument: ${token}`);
  }
  return parsed;
}

function intake(args) {
  const cwd = requireCwd(args.cwd);
  const task = requireArg(args.task, "--task");
  const taskId = requireArg(args.taskId, "--task-id");
  const epoch = requireArg(args.epoch, "--epoch");
  const scope = requireArg(args.scope, "--scope");
  const docsDir = path.join(cwd, "docs", "codex");
  mkdirSync(docsDir, { recursive: true });

  const context = {
    cwd,
    task,
    taskId,
    epoch,
    scope,
    docsDir,
    timestamp: new Date().toISOString(),
    gitStatus: readGitStatus(cwd),
  };

  for (const [file, body] of Object.entries(renderDocs(context))) {
    upsertGenerated(path.join(docsDir, file), body);
  }

  console.log(`ok intake: updated ${docsDir}`);
  console.log(`ok task_id: ${taskId}`);
  console.log(`ok epoch: ${epoch}`);
  console.log(`ok scope: ${scope}`);
}

function handoff(args) {
  const cwd = requireCwd(args.cwd);
  requireArg(args.taskId, "--task-id");
  const handoffPath = path.join(cwd, "docs", "codex", "handoff.md");
  if (!existsSync(handoffPath)) {
    throw new CliError(`missing handoff file: ${handoffPath}`, 1);
  }
  process.stdout.write(readFileSync(handoffPath, "utf8"));
}

