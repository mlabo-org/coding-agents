function verifyAssignments(args) {
  const cwd = requireCwd(args.cwd);
  const docsDir = path.join(cwd, "docs", "codex");
  const { results, fatal } = validateAssignmentFiles(docsDir);
  for (const [level, message] of results) {
    console.log(`${level} ${message}`);
  }
  process.exit(fatal ? 1 : 0);
}

function doctor(args) {
  const cwd = requireCwd(args.cwd);
  const docsDir = path.join(cwd, "docs", "codex");
  const results = [];
  let fatal = false;

  if (!existsSync(docsDir)) {
    results.push(["warn", `missing docs directory: ${docsDir}`]);
    fatal = true;
  } else {
    results.push(["ok", `docs directory exists: ${docsDir}`]);
  }

  for (const file of REQUIRED_FILES) {
    const filePath = path.join(docsDir, file);
    if (existsSync(filePath)) results.push(["ok", `required file exists: ${file}`]);
    else {
      results.push(["warn", `missing required file: ${file}`]);
      fatal = true;
    }
  }

  const assignmentValidation = validateAssignmentFiles(docsDir);
  results.push(...assignmentValidation.results);
  fatal = fatal || assignmentValidation.fatal;

  const taskPath = path.join(docsDir, "task.md");
  if (existsSync(taskPath)) {
    const text = readFileSync(taskPath, "utf8");
    for (const key of ["task_id", "epoch", "scope"]) {
      if (new RegExp(`${key}:`).test(text)) results.push(["ok", `${key} present in task.md`]);
      else {
        results.push(["warn", `${key} missing from task.md`]);
        fatal = true;
      }
    }
  }

  const gitStatus = readGitStatus(cwd);
  if (gitStatus.ok) {
    const status = gitStatus.output.trim();
    if (status) results.push(["warn", `git has uncommitted changes (${status.split("\n").length} line(s))`]);
    else results.push(["ok", "git working tree clean"]);
  } else {
    results.push(["warn", `git status unavailable: ${gitStatus.error}`]);
  }

  for (const [level, message] of results) {
    console.log(`${level} ${message}`);
  }
  process.exit(fatal ? 1 : 0);
}

