function getFieldValues(section, field) {
  const values = [];
  const block = getStructuralFieldBlocks(section)[0] || [];
  for (const entry of block) {
    if (entry.field !== field) continue;
    const value = entry.value.trim();
    if (value) values.push(value);
  }
  return values;
}

